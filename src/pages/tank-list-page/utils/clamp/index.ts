/**
 * Ограничивает число в заданных границах.
 *
 * Возвращает:
 * - `min`, если `n < min`
 * - `max`, если `n > max`
 * - `n`, если `min <= n <= max`
 *
 * @param n - Число, которое нужно ограничить.
 * @param min - Нижняя граница (включительно).
 * @param max - Верхняя граница (включительно).
 * @returns Число в диапазоне [`min`, `max`].
 *
 * @example
 * clamp(5, 1, 10);    // 5
 * clamp(-1, 0, 10);   // 0
 * clamp(999, 0, 10);  // 10
 *
 * @remarks
 * Функция не валидирует корректность диапазона.
 * Если `min > max`, результат может быть неочевидным
 * (например `clamp(5, 10, 1) === 1`).
 */
export const clamp = (n: number, min: number, max: number) => {
  return Math.min(max, Math.max(min, n));
};
